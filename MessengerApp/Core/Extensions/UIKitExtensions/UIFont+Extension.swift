import UIKit

extension UIFont {
    
    static func avenir20() -> UIFont? {
        return UIFont.init(name: "avenir", size: 20)
    }
    
    static func avenir26() -> UIFont? {
        return UIFont.init(name: "avenir", size: 26)
    }
    
    static func laoSangnamMN20() -> UIFont? {
        return UIFont.init(name: "Lao Sangnam MN", size: 20)
    }
    
    static func laoSangnamMN18() -> UIFont? {
        return UIFont.init(name: "Lao Sangnam MN", size: 18)
    }
    
}
